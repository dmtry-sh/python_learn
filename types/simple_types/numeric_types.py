print('# ints: ')
print('1 + 2 = ', 1 + 2 )
print('5 * 9 = ', 5 * 9 )
print('1 - 7 = ', 1 - 7 )
print('24 * (-3)', 24 * (-3))
print()

print('# floats:')
print('1.3 * 0.3 = ', 1.3 * 0.3 )
print('1.3 * .3 = ', 1.3 * .3 )
print('279 * .105 = ', 279 * .105 )
print('279 * 0.105 = ', 279 * 0.105)
print()

print('# mixed:')
print('1 + 1.5 = ', 1 + 1.5)
print('3 * 0.5 = ', 3 * 0.5)
print('7 / 3 = ', 7 / 3)
print('25 / 5 = ', 25 / 5)
print()

print('# complex:')
print('1j * 1j = ', 1j * 1j)
print('(1 + 1j) + (2 + 2j) = ', (1 + 1j) + (2 + 2j))
